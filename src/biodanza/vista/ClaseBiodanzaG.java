    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt)
    {// GEN-FIRST:event_jButton3ActionPerformed
        Ejercicio actual = (Ejercicio) listaEjercicios.getSelectedValue();
        if(actual != null)
        {
            int res = JOptionPane.showConfirmDialog(this, "Seguro desea borrar el ejercicio " + actual + "?", "Borrar",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(res == JOptionPane.YES_OPTION)
            {
                clase.eliminarEjercicio(actual);
                listaEjercicios.updateUI();
            }
        }
    }// GEN-LAST:event_jButton3ActionPerformed


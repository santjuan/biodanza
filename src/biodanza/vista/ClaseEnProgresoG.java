    public void keyPressed(KeyEvent e)
    {
        Cancion cActual = ejercicioV1.ejercicio == null ? null : ejercicioV1.ejercicio.getCancion();
        if(e.getKeyCode() == KeyEvent.VK_UP)
        {
            ejercicioV1.play();
        }
        else if(e.getKeyCode() == KeyEvent.VK_DOWN)
        {
            if(cActual == null)
                return;
            if(ejercicioV1.isPlaying() && !ejercicioV1.isPaused())
                ejercicioV1.pause();
            else if(ejercicioV1.isPlaying() && ejercicioV1.isPaused())
                ejercicioV1.stop();
        }
        else if(e.getKeyCode() == KeyEvent.VK_LEFT)
            jButton2ActionPerformed(null);
        else if(e.getKeyCode() == KeyEvent.VK_RIGHT)
            jButton1ActionPerformed(null);

    }


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt)
    {// GEN-FIRST:event_jButton2ActionPerformed
        if(ejercicioV1 != null && ejercicioV1.isPlaying())
            ejercicioV1.stop();
        if(ejercicioV2 != null && ejercicioV2.isPlaying())
            ejercicioV2.stop();
        actual--;
        if(actual == -1)
            actual++;
        recargarEjercicios();
    }// GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)
    {// GEN-FIRST:event_jButton1ActionPerformed
        if(ejercicioV1 != null && ejercicioV1.isPlaying())
            ejercicioV1.stop();
        if(ejercicioV2 != null && ejercicioV2.isPlaying())
            ejercicioV2.stop();
        actual++;
        if(actual == clase.darTamano())
            actual--;
        recargarEjercicios();
    }// GEN-LAST:event_jButton1ActionPerformed

    private void recargarEjercicios()
    {
        ejercicioV1.cambiarEjercicio(clase.darEjercicio(actual));
        ejercicioV2.cambiarEjercicio(actual == clase.darTamano() - 1 ? null : clase.darEjercicio(actual + 1));
        repaint();
    }

